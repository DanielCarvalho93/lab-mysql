CREATE TABLE
lab_mysql.Cars
(ID TINYINT AUTO_INCREMENT PRIMARY KEY,
VIN VARCHAR(52),
Manufactor VARCHAR(100),
Model VARCHAR(52) NOT NULL,
Car_Year YEAR,
Color VARCHAR(52));

CREATE TABLE
lab_mysql.Customers
(ID TINYINT AUTO_INCREMENT PRIMARY KEY,
Customer_ID INT,
Customer_Name VARCHAR(100) NOT NULL,
Phone_Number VARCHAR(52) NOT NULL,
Email VARCHAR(52),
Address VARCHAR(52),
City VARCHAR(52),
State VARCHAR(52),
Country VARCHAR(52),
Post_Code INT);

CREATE TABLE
lab_mysql.SalesPersons
(ID TINYINT AUTO_INCREMENT PRIMARY KEY,
Staff_ID TINYINT,
Staff_Name VARCHAR(100) NOT NULL,
Store VARCHAR(52));

CREATE TABLE
lab_mysql.Invoices
(ID TINYINT AUTO_INCREMENT PRIMARY KEY,
Invoice_Number TINYINT,
Purchase_Date DATE NOT NULL,
Car VARCHAR(52) NOT NULL,
Customer VARCHAR(52) NOT NULL,
Sales_Person VARCHAR(52));